function create_colors(lue::Float64, c::Float64, h::Array{Float64,1})
    return [OKcolor(Oklch(lue * 0.01, c, hue)) for hue in h]
end

function find_lightscheme(upperbound::Float64, lowerbound::Float64, u::Float64, l::Float64)::Bool
    hue = [315.0, 135.0]
    d0 = create_colors(upperbound - 3u, 0.015, [hue[1], hue[2]])
    d1 = create_colors(upperbound - 2u, 0.015, [hue[1], hue[2]])
    d1a = make_accents(d1[1], 0.03, 3)
    d2 = create_colors(upperbound - u, 0.015, [hue[1], hue[2]])
    d3 = create_colors(upperbound, 0.015, [hue[1], hue[2]])
    d3a = make_accents(d3[1], 0.03, 3)
    v3 = create_colors(lowerbound, 0.015, [hue[1], hue[2]])
    v2 = create_colors(lowerbound + l, 0.015, [hue[1], hue[2]])
    v2a = make_accents(v2[2], 0.03, 8)
    v1 = create_colors(lowerbound + 2l, 0.015, [hue[1], hue[2]])
    v0 = create_colors(lowerbound + 3l, 0.015, [hue[1], hue[2]])

    if cmp_contrasts(d0, v3, 7.0) && cmp_contrasts(vcat(d1, d1a), vcat(v2, v2a), 7.0) &&
       cmp_contrasts(d2, v1, 7.0) && cmp_contrasts(vcat(d3, d3a), v0, 7.0) && cmp_contrasts(vcat(d3, d3a), v3, 4.5)
        return true
    end
    return false

end

