function find_unsupport_color(colors::Vector{OKcolor})::Bool
    overrange = [(0.005 < i.rgb.r < 0.995) &&
                 (0.005 < i.rgb.b < 0.995) &&
                 (0.005 < i.rgb.g < 0.995) for i in colors]
    return all(x -> x, overrange)

end

